#include "minishell.h"

char	*ft_strcat(char *dest, const char *src)
{
	int	i;

	i = 0;
	while (dest[i]) // unconditional jump if here, if line is not initialized
		i++;
	while (*src)
	{
		dest[i] = *src;
		i++;
		src++;
	}
	dest[i] = '\0';
	return (dest);
}